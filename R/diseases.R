#' Make nodes for visNetwork
#'
#' @param df A dataframe from function makeConnections
#' @return A new dataframe of nodes that will be used in visNetwork
#' @example
#' nodes <- makeNodes(df)
#' @export
makeNodes <- function(df){
  df <- df[union(df$from[df$to > 0],df$to),]
  nodes <- data.frame(id = df$id,
                      group = df$trait,
                      label = df$id,
                      value = df$value,
                      shape = df$shape,
                      title = apply(df, 1, function(x)
                        paste(x["title_n"], x["trait"], sep = "\n")),
                      color = df$color
                      #shadow = FALSE
                      )
  return(nodes)
}

#' Make edges for visNetwork
#'
#' @param df A dataframe from function makeConnections()
#' @return A new dataframe with edges for visNetwork
#' @example
#' edges <- makeEdges(df)
#' @export
makeEdges <- function(df){
  edges <- data.frame(from = df$from,
                      to = df$to,
                      value = df$title_e,
                      #length = df$title_e,
                      #arrows = df$arrows,
                      #dashes = FALSE,
                      title = df$title_e
                      #smooth = FALSE,
                      #shadow = FALSE
                      )
  return(edges)
}

#' Get visNetwork plot
#'
#' @param data A dataframe from function makeConnections()
#' @param save Set to FALSE by default, must be specified as YES
#' to save a graph as html
#' @param path A path to a folder where the graph should be saved.
#' Will not be used if save is set to FALSE
#' @param name A name for plot to be saved in file. Will bot be used
#' if save is set to FALSE
#' @return A plot visNetwork graph
#' @examples
#' network <- getVisNetwork(data)
#' network <- getVisNetwork(data, TRUE, "C:/../home/test.html", "save.html")
#' @export
getVisNetwork <- function(data, len = 0, save = FALSE, path, name){
  if(nrow(data) == 0){
    print("There are no networks to show.")
  }
  library(visNetwork)
  data <- hideArrows(data, len)
  nodes <- makeNodes(data)
  edges <- makeEdges(data)
  v <-visNetwork(nodes, edges, width = "100%") %>%
    visClusteringOutliers(1)
  if(save){
    visSave(v, path,
            selfcontained = TRUE, background = "white")
  }
  return(v)
}

#' Generate infromation from connection data
#'
#' @param data A dataframe that was generated by makeConnections
#' @param id A single number or an array of numbers used in data made
#' for connections - id. Either id or study should be provided
#' @param study A single name or an array of study names that will be included
#' @return Returns a dataframe with id, connecting from id, id's study name,
#' id's trait, connecting to id, id's study name, trait and overlap count
#' between them - matching cg positions count
#' @examples
#' df <- getSimilarTraits(data2, study = c("ES00034, "ES00035))
#' df <- getSimilarTraits(data2, c(1,2,3,42))
#' @export
getSimilarTraits <- function(data, id = 0, study = ""){

  ifelse((length(id) == 1),
         ifelse((id == 0),
                id <- data$id[data$title_n %in% study][1], 0), 0)

  tdata <- data[(data$from %in% id) |
                ((data$from == 1) & (data$to %in% id)),]
  dat <- data.frame("id" = numeric(), "f.id" = numeric(),
                   "fStudy.id" = character(), "f.trait" = character(),
                   "t.id" = numeric(), "tStudy.id" = character(),
                   "t.trait" = character(), "overlaps" = numeric(),
                   stringsAsFactors = FALSE)
  for(i in 1:nrow(tdata)){
    tid <- tdata$to[i]
    dat[i,] <- c(tdata$id[i], tdata$from[i], tdata$title_n[i],
                 tdata$trait[i], tdata$to[i], data$title_n[tid],
                 data$trait[tid], tdata$title_e[i])
  }
  return(dat)
}

